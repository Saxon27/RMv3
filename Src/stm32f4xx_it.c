/**
* @brief This function handles TIM6 global interrupt, DAC1 and DAC2 underrun error interrupts.
*/
void TIM6_DAC_IRQHandler(void)
{
  /* USER CODE BEGIN TIM6_DAC_IRQn 0 */
  //Test_task();
  /* USER CODE END TIM6_DAC_IRQn 0 */
  HAL_TIM_IRQHandler(&htim6);
  /* USER CODE BEGIN TIM6_DAC_IRQn 1 */
//	ReadContral();
//	ReadEncode();
//	Contral();
	ReadControl();
	ReadEncode();
	NUM1 = SpeedPID(ecode1 , Speed1 , NUM1 , 0);
	NUM2 = SpeedPID(ecode2 , Speed2 , NUM2 , 1);
	NUM3 = SpeedPID(ecode3 , Speed3 , NUM3 , 2);
	NUM4 = SpeedPID(ecode4 , Speed4 , NUM4 , 3);
	NUM5 = PitchPID();
	NUM6 = 0 ;
  CAN_Send_Msg(&hcan1 , NUM1 , NUM2 , NUM3 , NUM4 , 0x200 , 0x08 );
	CAN_Send_Msg(&hcan1 , NUM5 , NUM6 , 0 , 0 , 0x1FF , 0x08 );
  /* USER CODE END TIM6_DAC_IRQn 1 */
}

